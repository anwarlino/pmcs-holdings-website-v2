const fallbackLanguage = 'en';
const supportedLanguages = ['en', 'th', 'ar'];

function getValue(source, path) {
  return path.split('.').reduce((value, key) => (value === undefined || value === null ? undefined : value[key]), source);
}

function translate(language, path) {
  return getValue(languageDictionary[language], path) ?? getValue(languageDictionary[fallbackLanguage], path) ?? '';
}

function applyLanguage(language) {
  const activeLanguage = supportedLanguages.includes(language) ? language : fallbackLanguage;
  const isRtl = activeLanguage === 'ar';
  document.documentElement.lang = activeLanguage;
  document.documentElement.dir = isRtl ? 'rtl' : 'ltr';
  document.body.dir = isRtl ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const value = translate(activeLanguage, element.dataset.i18n);
    if (value) element.textContent = value;
  });

  document.querySelectorAll('[data-i18n-attr]').forEach((element) => {
    element.dataset.i18nAttr.split(',').forEach((pair) => {
      const [attribute, key] = pair.split(':').map((part) => part.trim());
      const value = translate(activeLanguage, key);
      if (attribute && value) element.setAttribute(attribute, value);
    });
  });

  document.title = translate(activeLanguage, 'meta.title');
  localStorage.setItem('pmcs-language', activeLanguage);
  const selector = document.querySelector('#languageSelect');
  if (selector) selector.value = activeLanguage;
  updateMenuLabel();
}

function updateMenuLabel() {
  const toggle = document.querySelector('.menu-toggle');
  const selector = document.querySelector('#languageSelect');
  if (!toggle) return;
  const activeLanguage = selector ? selector.value : fallbackLanguage;
  const isOpen = toggle.getAttribute('aria-expanded') === 'true';
  toggle.setAttribute('aria-label', translate(activeLanguage, isOpen ? 'nav.closeMenu' : 'nav.openMenu'));
}

function setupNavigation() {
  const toggle = document.querySelector('.menu-toggle');
  const menu = document.querySelector('.site-menu');
  if (!toggle || !menu) return;

  toggle.addEventListener('click', () => {
    const isOpen = toggle.getAttribute('aria-expanded') === 'true';
    toggle.setAttribute('aria-expanded', String(!isOpen));
    menu.classList.toggle('open', !isOpen);
    document.body.classList.toggle('nav-open', !isOpen);
    updateMenuLabel();
  });

  menu.querySelectorAll('a').forEach((link) => {
    link.addEventListener('click', () => {
      toggle.setAttribute('aria-expanded', 'false');
      menu.classList.remove('open');
      document.body.classList.remove('nav-open');
      updateMenuLabel();
    });
  });
}

function setupLanguageSwitcher() {
  const selector = document.querySelector('#languageSelect');
  if (!selector) return;
  const savedLanguage = localStorage.getItem('pmcs-language');
  applyLanguage(supportedLanguages.includes(savedLanguage) ? savedLanguage : fallbackLanguage);
  selector.addEventListener('change', (event) => applyLanguage(event.target.value));
}

function setupLogoFallback() {
  const logo = document.querySelector('.brand-logo');
  const brand = document.querySelector('.brand');
  if (!logo || !brand) return;
  const showFallback = () => brand.classList.add('logo-missing');
  logo.addEventListener('error', showFallback);
  if (logo.complete && logo.naturalWidth === 0) showFallback();
}

function setupImageFallbacks() {
  document.querySelectorAll('.managed-image').forEach((image) => {
    const showPlaceholder = () => {
      image.classList.add('image-missing');
      const frame = image.closest('.image-frame');
      if (frame) frame.classList.add('image-missing');
    };
    image.addEventListener('error', showPlaceholder);
    if (image.complete && image.naturalWidth === 0) showPlaceholder();
  });
}

function setupRevealAnimation() {
  const elements = document.querySelectorAll('.reveal');
  if (!('IntersectionObserver' in window)) {
    elements.forEach((element) => element.classList.add('visible'));
    return;
  }
  const observer = new IntersectionObserver((entries, revealObserver) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.14 });
  elements.forEach((element) => observer.observe(element));
}

function setupFooterYear() {
  const year = document.querySelector('#year');
  if (year) year.textContent = new Date().getFullYear();
}

document.addEventListener('DOMContentLoaded', () => {
  setupFooterYear();
  setupLogoFallback();
  setupImageFallbacks();
  setupNavigation();
  setupLanguageSwitcher();
  setupRevealAnimation();
});
